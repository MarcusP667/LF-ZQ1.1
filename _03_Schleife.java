
public class _03_Schleife {

	public static void main(String[] args) {
		// ASCII von A - Z 
		int start = 65;
		int end = 90;
		
		// Ausgabe Schleife
		
		for (int Ausgabe = start; Ausgabe <= end; Ausgabe++) {
			char letter = (char) Ausgabe; // Zahlen in Buchstaben umwandeln
		System.out.println(letter);
			
		}

	}

}
